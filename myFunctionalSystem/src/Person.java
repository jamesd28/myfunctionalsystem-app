public class Person {
    private int ID;
    private String firstName;
    private String midName;
    private String lastName;
    private String prefName;
    private String email;
    private String netID;
    private String gender;
    private String birthday;
    private String phoneNum;
    private String cellNum;
    private String address;
    private String password;
    private String creationDate;
    private String modificationDate;
   
    public int getID() { return ID; }
    public String getfirstName() { return firstName; }
    public String getmidName() { return midName; }
    public String getLastName() { return lastName; }
    public String getprefName() { return prefName; } 
    public String getEmail() { return email; }
    public String getNetID() { return netID; }
    public String getGender() { return gender; }
    public String getBirthday() { return birthday; }
    public String getPhoneNum() { return phoneNum; }
    public String getcellNum() { return cellNum; }
    public String getAddress() { return address; }
    public String getPassword() { return password; }
    public String getCreationDate() { return creationDate; }
    public String getModificationDate() { return modificationDate; }
    
    public void setID(int ID) { this.ID = ID; }
    public void setfirstName(String firstName) { this.firstName = firstName; }
    public void setmidName(String midName) { this.midName = midName; }
    public void setLastName(String lastName) { this.lastName = lastName; }
    public void setprefName(String prefName) { this.prefName = prefName; } 
    public void setEmail(String email) { this.email = email; }
    public void setNetID(String netID) { this.netID = netID; }
    public void setGender(String gender) { this.gender = gender; }
    public void setBirthday(String birthday) { this.birthday = birthday; }
    public void setPhoneNum(String phoneNum) { this.phoneNum = phoneNum; }
    public void setcellNum(String cellNum) { this.cellNum = cellNum; }
    public void setAddress(String address) { this.address = address; }
    public void setPassword(String password) { this.password = password; }
    public void setCreationDate(String creationDate) { this.creationDate = creationDate; }
    public void setModificationDate(String modificationDate) { this.modificationDate = modificationDate; }
}

